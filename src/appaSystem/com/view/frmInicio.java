    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        con.conecta(dcon.getUrl(), dcon.getDriver(), dcon.getUsuario(), dcon.getSenha());
        String sql = "SELECT NOME FROM CONTATO WHERE COD = "+txtCodigo.getText();
        
        con.executeConsulta(sql);
        
        int resp = JOptionPane.showConfirmDialog(null, "Deseja realmente eliminar o registro "+sql+"?");
        
        if(resp == JOptionPane.YES_OPTION){
            sql = "DELETE FROM CONTATO WHERE COD = "+txtCodigo.getText();
            
            con.executeAtualizacao(sql);
            con.desconecta();
        
            txtNome.setEnabled(false);
            btnEditarNome.setEnabled(true);
            btnSalvarNome.setEnabled(false);
            btnCancelaNome.setEnabled(false);
            
            txtPesquisar.setText("");
            txtCodigo.setText("");
            txtNome.setText("");
            txtTelefone.setText("");
        
            montaLista();
            
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

