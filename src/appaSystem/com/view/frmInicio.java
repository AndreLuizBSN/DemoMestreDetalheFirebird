    public void montaLista(){
        
        this.lista.setColumnCount(0);
        this.lista.setRowCount(0);
        
        jTable1.setModel(this.lista);
        
        con.conecta(dcon.getUrl(), dcon.getDriver(), dcon.getUsuario(), dcon.getSenha());
        String sql = "SELECT * FROM CONTATO";
        
        if(!txtPesquisar.getText().equals("")){
            
            sql +=" WHERE UPPER(NOME) LIKE '%"+txtPesquisar.getText().toUpperCase()+"%'"
                    +" OR TEL LIKE '%"+txtPesquisar.getText()+"%'";
            
        }
        
        sql += " ORDER BY NOME";
        
        con.executeConsulta(sql);
            
        lista.addColumn("Código");
        lista.addColumn("Nome");
        lista.addColumn("Telefone");

        try{
            while(con.rs.next()){
                lista.addRow(new String[]{
                    con.rs.getString("COD"),
                    con.rs.getString("NOME"),
                    con.rs.getString("TEL")
                });
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Erro ao carregar dados para exibir na tabela!\n"+ex);
        }
        
        con.desconecta();
        jTable1.setModel(lista);

        
        
    }
    
    public void selecao(){
        
        int linha = jTable1.getSelectedRow();
        
        txtCodigo.setText(jTable1.getValueAt(linha, 0).toString());
        txtNome.setText(jTable1.getValueAt(linha, 1).toString());
        txtTelefone.setText(jTable1.getValueAt(linha, 2).toString());
        
        
    }
    
